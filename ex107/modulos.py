def aumentar(preco):
    preco = preco + (preco * 0.1)
    print(f'O preço aumentado em 10% é: {preco}')


def diminuir(preco):
    preco = preco * 0.9
    print(f'O preço diminuido em 10% é: {preco}')


def metade(preco):
    preco = preco / 2
    print(f'A metade do preço é {preco}')


def dobro(preco):
    preco = preco * 2
    print(f'O dobro do preco é {preco}')